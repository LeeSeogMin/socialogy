/**
 * 객관식 문항 추가 헬퍼
 * @param {Form} form - 폼 객체
 * @param {string} title - 문항 제목
 * @param {string} helpText - 문항 설명
 * @param {string[]} choices - 선택지 배열
 * @param {number} correctIndex - 정답 인덱스 (0-based)
 * @param {number} points - 배점
 */
function addMC(form, title, helpText, choices, correctIndex, points) {
  var item = form.addMultipleChoiceItem();
  item.setTitle(title);
  item.setHelpText(helpText);
  item.setRequired(true);
  item.setPoints(points);

  var choiceObjects = [];
  for (var i = 0; i < choices.length; i++) {
    if (i === correctIndex) {
      choiceObjects.push(item.createChoice(choices[i], true));
    } else {
      choiceObjects.push(item.createChoice(choices[i], false));
    }
  }
  item.setChoices(choiceObjects);
}
